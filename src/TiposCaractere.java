// Escreva um programa que declare 6 variaveis do tipo caractere e atribua
// a elas as letras a, l, u, n, o, s. O programa devera imprimir, usando
// todas as variaveis declaradas, o seguinte resultado:
// a + l + u + n + o + s.

public class TiposCaractere {

    public static void main(String[] args) {
    // declaracao das variaveis do tipo caractere
      char c1, c2, c3, c4, c5, c6;

    // atribuindo valores as variaveis
      c1 = 'a';
      c2 = 'l';
      c3 = 'u';
      c4 = 'n';
      c5 = 'o';
      c6 = 's';

    // usando os valores armazenados nas variaveis
      System.out.println(" " + c1 + c2 + c3 + c4 + c5 + c6);
      
    }

}